import { DataTypes, Model } from "sequelize";
import sequelize from "../config/sequelize.js";

class UserModel extends Model {}

UserModel.init(
  {
    name: {
      type: DataTypes.STRING,
      allowNull: false,
    },
    email: {
      type: DataTypes.STRING,
      unique: true,
      allowNull: false,
    },
    password: {
      type: DataTypes.STRING,
      allowNull: false,
    },
  },
  {
    sequelize,
    modelName: "User",
  }
);

export default UserModel;
